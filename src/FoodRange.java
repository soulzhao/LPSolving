
public class FoodRange {
	float max;
	float min;
	
	public float getMax() {
		return max;
	}
	public void setMax(float max) {
		this.max = max;
	}
	public float getMin() {
		return min;
	}
	public void setMin(float min) {
		this.min = min;
	}
	public FoodRange(float max, float min) {
		super();
		this.max = max;
		this.min = min;
	}
	
	
}
